		<h3>
			Modern History
		</h3>
		<p>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The modern history of cemeteries began during the 18th century. The cemetery reform movement was spearheaded by the London Society for the Abolition of Burials in Towns, founded in 1842. The society advocated for the establishment of large municipal cemeteries on the outskirts of town. In 1847, the British Parliament passed the Burial Act, which empowered local authorities to purchase land to be used as cemeteries. The act also required that all corpses be buried in airtight coffins. The act was further amended in 1853 to require that all burial grounds be closed to the public and that all burials take place at night. The act was eventually extended to cover the whole of England and Wales by 1855. The provisions of the Burial Act were also incorporated into the Burial Laws Amendment Act 1880, which also prohibited the burial of corpses in places not designated as cemeteries.
